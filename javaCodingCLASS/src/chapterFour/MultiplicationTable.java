package chapterFour;

public class MultiplicationTable {
    public static void main(String [] args ){
        int counter = 1;


        for( ; counter <= 20; counter++){

            for (int counterb = 1; counterb <= 20; counterb++){

                System.out.print( counter * counterb + "\t\t"  );


            }


            System.out.println();


        }









    }


}
